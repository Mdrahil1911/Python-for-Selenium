#Range function is python

print(range(10))    #prints 0......9 as last digit conidered as n-1
#to print the numbers we need to use word called "list"
print(list(range(10)))

range(1,10)  #prints 1....9 (n-1)
print(list(range(1,10)))

range(0,10,2)   #prints 0.....10(n-1) with increment of 2
print(list(range(0,10,2)))

#printing numbers in reverse order
print(list(range(10,1,-1)))
